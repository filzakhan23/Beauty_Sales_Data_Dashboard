# Beauty_Sales_Data_Dashboard
💄 Beauty Sales Data Dashboard
My first-ever Power BI dashboard, built from scratch with zero prior BI experience. This
project took a raw Kaggle dataset through Python cleaning and into an interactive,
KPI-driven dashboard.
![Dashboard Screenshot](./dashboard_screenshot.png)
📊 Overview
This dashboard explores global cosmetics and skincare sales data, answering questions like:
How does revenue trend month over month?
Which countries generate the most sales?
Which products are top performers?
What's the average order value and total order volume?
🔑 Key Features
KPI Cards — Total Revenue, Boxes Shipped, Average Order Value, Total Orders
Monthly Revenue Trend — line chart tracking sales by month
Geographic Breakdown — revenue by country (USA, New Zealand, Australia, UK, Canada, India)
Product Performance — top-performing products by sales volume
Interactive Slicers — filter by Year, Country, and Product
🛠️ Tools & Process
Python (Pandas) — cleaned and prepped the raw dataset before loading it into Power BI
Power BI — built the interactive dashboard: KPI cards, trend lines, bar charts, and slicers
📁 Dataset
Cosmetics & Skincare Product Sales Data (2022) — Kaggle
A global transactional dataset simulating real-world sales of cosmetic and skincare products.
